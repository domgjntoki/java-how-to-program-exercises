public class StringConcatenation {
    public static void main(String[] args) {
        String s1 = "Happy ";
        String s2 = "Birthday";
        System.out.printf("Result of s1.concat(s2): %s%n", s1.concat(s2));
        System.out.println("s1 after concatenation: " + s1);
    }
}
